var val = "1";

// Type Conversion from String to Integer before Checking Equality
if (val == 1)
    console.log("\"1\" == 1, returns True")
else
    console.log("\"1\" == 1, returns False")

// Type Conversion from String to Boolean
if (val == true)
console.log("\"1\" == true, returns True")
else
console.log("\"1\" == true, returns False")    

// Type Conversion from String to Boolean
if (1 === true)
console.log("1 === true, returns True")
else
console.log("1 === true, returns False")    

// Strict equality check    
if (val === 1)
    console.log("\"1\" === 1, returns True")
else
    console.log("\"1\" === 1, returns False")
